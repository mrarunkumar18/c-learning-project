#include <stdio.h>
int main()
{
    int a = 1; // integer
    // float b = 1.4; // floating point number
    float b;
    b = 1.4; // floating point number
    char c;
    c = 'a'; // character

    printf("The value of a is %d\n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);


    return 0;
}